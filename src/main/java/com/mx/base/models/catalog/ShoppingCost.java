package com.mx.base.models.catalog;

public class ShoppingCost {
	
	public double amount;
	public boolean applyClient;
	
	public double getAmount() {
		return amount;
	}
	public void setAmount(double amount) {
		this.amount = amount;
	}
	public boolean isApplyClient() {
		return applyClient;
	}
	public void setApplyClient(boolean applyClient) {
		this.applyClient = applyClient;
	}

}
